package chainofresponsabilities;

public class ProductsMachine {

	private int gansitos;
	private int nitos;
	private int lollipops;
	private double money;
	
	
	
	public double getMoney() {
		return money;
	}
	public void setMoney(double money) {
		this.money = money;
	}
	public int getGansitos() {
		return gansitos;
	}
	public void setGansitos(int gansitos) {
		this.gansitos = gansitos;
	}
	public int getNitos() {
		return nitos;
	}
	public void setNitos(int nitos) {
		this.nitos = nitos;
	}
	public int getLollipops() {
		return lollipops;
	}
	public void setLollipops(int lollipops) {
		this.lollipops = lollipops;
	}
	
}
